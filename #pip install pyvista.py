#coordinates to vtk
import pyvista as pv
import vtk
import numpy as np
from pathlib import Path
from flask import Flask, send_file

app = Flask(__name__)

def create_mesh_from_coordinates(coordinates):
    # Convert coordinates to numpy array
    points = np.array(coordinates)

    # Create a PyVista mesh
    mesh = pv.PolyData()
    mesh.points = points

    # Perform a Delaunay triangulation to create a surface mesh
    surf = mesh.delaunay_3d()

    return surf

def save_mesh_vtk(mesh, filename):
    # Save the mesh to a VTK file
    mesh.save(filename)

def convert_to_obj(input_vtk_file, output_obj_file):
    # Load the VTK file
    reader = vtk.vtkUnstructuredGridReader()
    reader.SetFileName(input_vtk_file)
    reader.Update()

    # Create an OBJ writer
    obj_writer = vtk.vtkOBJWriter()
    obj_writer.SetFileName(output_obj_file)
    obj_writer.SetInputData(reader.GetOutput())
    obj_writer.Update()

    print("Conversion to OBJ completed successfully. OBJ file saved as", output_obj_file)

def download_obj(obj_file_path):
    return send_file(obj_file_path, as_attachment=True)

def plot_mesh(mesh):
    plotter = pv.Plotter()
    plotter.add_mesh(mesh, color='red', opacity=0.5)
    plotter.show()

